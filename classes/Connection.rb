class Connection
	
	def user
		@user
	end
	
	def user=(user)
		@user = user;
	end
	
	def password
		@password
	end

	def password=(password)
		@password = password
	end
	
	def ipAddress
		@ipAddress
	end
	
	def ipAddress=(ipAddress)
		@ipAddress = ipAddress
	end
	
	def databaseName
		@databaseName
	end
	
	def databaseName=(databaseName)
		@databaseName = databaseName;
	end						


end